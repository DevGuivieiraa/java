public class EstruturasDeControle {
    public static void main(String[] args) {
        int nota = 5;

        if (nota >= 7){
            System.out.println("Aprovado");
        }else if (nota >= 3){
            System.out.println("Em exame");
        }else{
            System.out.println("Reprovado");
        }
    }
}
